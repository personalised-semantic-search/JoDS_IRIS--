<h1>Dependencies</h1>

<p>The libraries required depend on the intended use of
IRIS:</p>

<h2>Using the Binaries for Reasoning</h2>

<p>If you only use the precompiled binary release (e.g. in
conjunction with wsml2reasoner) the following third party
libraries are required:</p>

<ul>
    <li><a href=
    "http://jgrapht.sourceforge.net/">jgraph</a> JGraphT is
    a free (LGPL) Java graph library that provides
    mathematical graph-theory objects and algorithms.</li>

    <li><a href=
    "http://logging.apache.org/log4j/docs/">log4j</a> Log4j
    provides a customizable logging service for debugging
    and auditing.</li>
</ul>

<h2>Compiling From Source</h2>

<p>In order to generate the build-in parser for IRIS and
run the unit tests the following third party libraries are
required:</p>

<ul>
    <li><a href="http://sablecc.org/">sableCC</a> SableCC
    is a parser generator which generates fully featured
    object-oriented frameworks for building compilers,
    interpreters and other text parsers.</li>

    <li><a href="http://www.junit.org/">JUnit</a> JUnit is
    a regression testing framework written by Erich Gamma
    and Kent Beck. It is used by the developer who
    implements unit tests in Java.</li>
</ul>
